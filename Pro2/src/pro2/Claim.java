    private void formComponentShown(java.awt.event.ComponentEvent evt) {//GEN-FIRST:event_formComponentShown
        // TODO add your handling code here:
        DefaultTableModel model = (DefaultTableModel) tableClaim.getModel();
        try {
            Connection con = ConnectionProvider.getCon();
            Statement st = con.createStatement();
            ResultSet rs = st.executeQuery("select *from Claim inner join Policy on Claim.poli_ID=Policy.poli_ID");
            while (rs.next()) {
                model.addRow(new Object[]{rs.getString("claim_ID"), rs.getString("claim_date"), rs.getString("claim_Amount"), rs.getString("claim_status"), rs.getString("poli_ID")});
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, e);
        }
        btnUpdate.setEnabled(false);
    }//GEN-LAST:event_formComponentShown

