    private void tableClaimMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tableClaimMouseClicked
        // TODO add your handling code here:
        int index = tableClaim.getSelectedRow();
        TableModel model = tableClaim.getModel();

        String id = model.getValueAt(index, 0).toString();
        Claimpk = Integer.parseInt(id);

        String name = model.getValueAt(index, 1).toString();
        txtdate.setText(name);

        String mobileno = model.getValueAt(index, 2).toString();
        txtamount.setText(mobileno);


        String status = model.getValueAt(index, 3).toString();
        ComboBoxStatus.removeAllItems();
        if (status.equals("Claimed")) {
            ComboBoxStatus.addItem("Claimed");
            ComboBoxStatus.addItem("Under Process");
            ComboBoxStatus.addItem("Claim Rejeted");
        } else {
           ComboBoxStatus.addItem("Claim Rejeted");
            ComboBoxStatus.addItem("Under Process");
            ComboBoxStatus.addItem("Claimed");
        }

            
        String poli_id = model.getValueAt(index, 4).toString();
        txtpoliID.setText(poli_id);

        btnSave.setEnabled(false);
        btnUpdate.setEnabled(true);
    }//GEN-LAST:event_tableClaimMouseClicked

    private void btnSaveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSaveActionPerformed
        // TODO add your handling code here:
        String Type = txtdate.getText();
        String amount = txtamount.getText();
        String status = (String) ComboBoxStatus.getSelectedItem();
        String poli_ID = txtpoliID.getText();


        if (validatetoFields("new")) {
            JOptionPane.showMessageDialog(null, "All fields are required");
        } else {
            try {
                Connection con = ConnectionProvider.getCon();
                PreparedStatement pe = con.prepareStatement("insert into Claim(claim_date,claim_Amount,claim_status,poli_ID) values (?,?,?,?)");
                pe.setString(1, Type);
                pe.setString(2, amount);
                pe.setString(3, status);
                pe.setString(4, poli_ID);

                pe.executeUpdate();
                
                JOptionPane.showMessageDialog(null, "customer Added Successfully.");
                setVisible(false);
                new Claim().setVisible(true);
            } catch (Exception e) {
                JOptionPane.showMessageDialog(null, e);
            }
        }
    }//GEN-LAST:event_btnSaveActionPerformed

    private void btnUpdateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnUpdateActionPerformed
        // TODO add your handling code here:
        String Type = txtdate.getText();
        String Sdate = txtamount.getText();
        String status = (String) ComboBoxStatus.getSelectedItem();
         String poli_id = txtpoliID.getText();
        
        if (validatetoFields("edit")) {
            JOptionPane.showMessageDialog(null, "All fields are required");
        } else {
            try {
                Connection con = ConnectionProvider.getCon();
                PreparedStatement pe = con.prepareStatement("update claim set claim_date=?,claim_Amount=?,claim_status=?,poli_ID=? where claim_ID=?");
                pe.setString(1,Type);
                pe.setString(2, Sdate);
                pe.setString(3, status);
                pe.setString(4, poli_id);
                pe.setInt(5, Claimpk);
                pe.executeUpdate();
                JOptionPane.showMessageDialog(null, "User Updated Successfully.");
                setVisible(false);
                new Claim().setVisible(true);
            } catch (Exception e) {
                JOptionPane.showMessageDialog(null, e);
            }
        }
    }//GEN-LAST:event_btnUpdateActionPerformed

