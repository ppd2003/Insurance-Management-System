    private void btnUpdateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnUpdateActionPerformed
        // TODO add your handling code here:
        String name = txtName.getText();
        String address = txtAdd.getText();
        String email = txtEmail.getText();
        String Mobileno = txtMobileNo.getText();
        String DOB = txtDOB.getText();
//        String status = (String) ComboBoxStatus.getSelectedItem();

        if (validatetoFields("edit")) {
            JOptionPane.showMessageDialog(null, "All fields are required");
        } else {
            try {
                Connection con = ConnectionProvider.getCon();
                PreparedStatement pe = con.prepareStatement("update Customer set Cus_name=?,Cus_Add=?,Cus_email=?,Cus_MobileNo=?,Cus_dob=? where Cus_ID=?");
                pe.setString(1, name);
                pe.setString(2, address);
                pe.setString(3, email);
                pe.setString(4, Mobileno);
                pe.setString(5, DOB);
                pe.setInt(6, Cust_pk);
                pe.executeUpdate();
                JOptionPane.showMessageDialog(null, "User Updated Successfully.");
                setVisible(false);
                new Customer().setVisible(true);
            } catch (Exception e) {
                JOptionPane.showMessageDialog(null, e);
            }
        }
    }//GEN-LAST:event_btnUpdateActionPerformed

    private void tableCustomerMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tableCustomerMouseClicked
        // TODO add your handling code here:
         int index = tableCustomer.getSelectedRow();
         TableModel model = tableCustomer.getModel();

        String id = model.getValueAt(index, 0).toString();
        Cust_pk = Integer.parseInt(id);

        String name = model.getValueAt(index, 1).toString();
        txtName.setText(name);
        
        String address = model.getValueAt(index, 2).toString();
        txtAdd.setText(address);
      
        String email = model.getValueAt(index, 3).toString();
        txtEmail.setText(email);
        
        String mobileno = model.getValueAt(index, 4).toString();
        txtMobileNo.setText(mobileno);

        String dob = model.getValueAt(index, 5).toString();
        txtDOB.setText(dob);
        
        
        btnSave.setEnabled(false);
        btnUpdate.setEnabled(true);
       
    }//GEN-LAST:event_tableCustomerMouseClicked

    private void btnDeleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDeleteActionPerformed
        // TODO add your handling code here:
        try {
            Connection con = ConnectionProvider.getCon();
            PreparedStatement pe = con.prepareStatement("delete from Customer where Cus_ID=?");
            pe.setInt(1, Cust_pk);
            pe.executeUpdate();
            JOptionPane.showMessageDialog(null, "Usr Deleted Successfully.");
            setVisible(false);
            new Customer().setVisible(true);
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, e);
        }
    }//GEN-LAST:event_btnDeleteActionPerformed

    private void formComponentShown(java.awt.event.ComponentEvent evt) {//GEN-FIRST:event_formComponentShown
        // TODO add your handling code here:
        DefaultTableModel model = (DefaultTableModel) tableCustomer.getModel();
        try {
            Connection con = ConnectionProvider.getCon();
            Statement st = con.createStatement();
            ResultSet rs = st.executeQuery("select *from Customer");
            while (rs.next()) {
                model.addRow(new Object[]{rs.getString("Cus_ID"), rs.getString("Cus_name"), rs.getString("Cus_Add"), rs.getString("Cus_email"), rs.getString("Cus_MobileNo"), rs.getString("Cus_dob")});
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, e);
        }
        btnUpdate.setEnabled(false);
        
    }//GEN-LAST:event_formComponentShown

