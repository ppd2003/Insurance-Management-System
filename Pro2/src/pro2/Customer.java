    private void btnSaveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSaveActionPerformed
        // TODO add your handling code here:
        String name = txtName.getText();
        String address = txtAdd.getText();
        String email = txtEmail.getText();
        String mobileno = txtMobileNo.getText();
        String DOB = txtDOB.getText();


        if (validatetoFields("new")) {
            JOptionPane.showMessageDialog(null, "All fields are required");
        } else {
            try {
                Connection con = ConnectionProvider.getCon();
                PreparedStatement pe = con.prepareStatement("insert into Customer(Cus_name,Cus_Add,Cus_email,Cus_MobileNo,Cus_dob) values (?,?,?,?,?)");
                pe.setString(1, name);
                pe.setString(2, address);
                pe.setString(3, email);
                pe.setString(4, mobileno);
                pe.setString(5, DOB);

                pe.executeUpdate();
                
                JOptionPane.showMessageDialog(null, "customer Added Successfully.");
                setVisible(false);
                new Customer().setVisible(true);
            } catch (Exception e) {
                JOptionPane.showMessageDialog(null, e);
            }
        }
    }//GEN-LAST:event_btnSaveActionPerformed

