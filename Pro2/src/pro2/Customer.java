    private boolean validatetoFields(String formType) {
        if (formType.equals("edit") && !txtName.getText().equals("") && !txtAdd.getText().equals("") && !txtEmail.getText().equals("") && !txtMobileNo.getText().equals("")  ) {
            return false;
        } else if (formType.equals("new") && !txtName.getText().equals("") && !txtAdd.getText().equals("") && !txtEmail.getText().equals("") && !txtMobileNo.getText().equals("") && !txtDOB.getText().equals("")) {
            return false;
        } else {
            return true;
        }
    }

