    private boolean validatetoFields(String formType) {
        if (formType.equals("edit") && !txtname.getText().equals("") && !txtmobileno.getText().equals("") && !txtemail.getText().equals("") && !txtaddress.getText().equals("")) {
            return false;
        } else if (formType.equals("new") && !txtname.getText().equals("") && !txtmobileno.getText().equals("") && !txtemail.getText().equals("") && !txtaddress.getText().equals("") && !txtpassword.getText().equals("")) {
            return false;
        } else {
            return true;
        }
    }

