    private void btnsaveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnsaveActionPerformed
        // TODO add your handling code here:
        String name = txtname.getText();
        String Mobileno = txtmobileno.getText();
        String email = txtemail.getText();
        String password = txtpassword.getText();
        String address = txtaddress.getText();
        String status = (String) ComboBoxStatus.getSelectedItem();

        if (validatetoFields("new")) {
            JOptionPane.showMessageDialog(null, "All fields are required");
        } else {
            try {
                Connection con = ConnectionProvider.getCon();
                PreparedStatement pe = con.prepareStatement("insert into aapuser(userRole,name,mobilenumber,email,password,address,status) values ('Admin',?,?,?,?,?,?)");
                pe.setString(1, name);
                pe.setString(2, Mobileno);
                pe.setString(3, email);
                pe.setString(4, password);
                pe.setString(5, address);
                pe.setString(6, status);
                pe.executeUpdate();
                JOptionPane.showMessageDialog(null, "Usr Added Successfully.");
                setVisible(false);
                new ManageUser().setVisible(true);
            } catch (Exception e) {
                JOptionPane.showMessageDialog(null, e);
            }
        }

    }//GEN-LAST:event_btnsaveActionPerformed

    private void TableuserMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_TableuserMouseClicked
        // TODO add your handling code here:
        int index = Tableuser.getSelectedRow();
        TableModel model = Tableuser.getModel();

        String id = model.getValueAt(index, 0).toString();
        appuserPk = Integer.parseInt(id);

        String name = model.getValueAt(index, 1).toString();
        txtname.setText(name);

        String mobileno = model.getValueAt(index, 2).toString();
        txtmobileno.setText(mobileno);

        String email = model.getValueAt(index, 3).toString();
        txtemail.setText(email);

        String address = model.getValueAt(index, 4).toString();
        txtaddress.setText(address);

        String status = model.getValueAt(index, 5).toString();
        ComboBoxStatus.removeAllItems();
        if (status.equals("Active")) {
            ComboBoxStatus.addItem("Active");
            ComboBoxStatus.addItem("Inactive");
        } else {
            ComboBoxStatus.addItem("Inactive");
            ComboBoxStatus.addItem("Active");
        }

        txtpassword.setEditable(false);
        txtpassword.setBackground(Color.DARK_GRAY);

        btnsave.setEnabled(false);
        btnupdate.setEnabled(true);

    }//GEN-LAST:event_TableuserMouseClicked

    private void btnupdateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnupdateActionPerformed
        // TODO add your handling code here:
        String name = txtname.getText();
        String Mobileno = txtmobileno.getText();
        String email = txtemail.getText();
//        String password=txtpassword.getText();
        String address = txtaddress.getText();
        String status = (String) ComboBoxStatus.getSelectedItem();

        if (validatetoFields("edit")) {
            JOptionPane.showMessageDialog(null, "All fields are required");
        } else {
            try {
                Connection con = ConnectionProvider.getCon();
                PreparedStatement pe = con.prepareStatement("update aapuser set name=?,mobilenumber=?,email=?,address=?,status=? where appuser_pk=?");
                pe.setString(1, name);
                pe.setString(2, Mobileno);
                pe.setString(3, email);
                pe.setString(4, address);
                pe.setString(5, status);
                pe.setInt(6, appuserPk);
                pe.executeUpdate();
                JOptionPane.showMessageDialog(null, "User Updated Successfully.");
                setVisible(false);
                new ManageUser().setVisible(true);
            } catch (Exception e) {
                JOptionPane.showMessageDialog(null, e);
            }
        }
    }//GEN-LAST:event_btnupdateActionPerformed

